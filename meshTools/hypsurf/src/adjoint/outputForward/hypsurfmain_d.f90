    do i=1,numnodes
      xd = rd(3*(i-1)+1)
      x = r(3*(i-1)+1)
      if (x .gt. maxx) then
        maxxd = xd
        maxx = x
      end if
      if (x .lt. minx) then
        minxd = xd
        minx = x
      end if
      yd = rd(3*(i-1)+2)
      y = r(3*(i-1)+2)
      if (y .gt. maxy) then
        maxyd = yd
        maxy = y
      end if
      if (y .lt. miny) then
        minyd = yd
        miny = y
      end if
      zd = rd(3*(i-1)+3)
      z = r(3*(i-1)+3)
      if (z .gt. maxz) then
        maxzd = zd
        maxz = z
      end if
      if (z .lt. minz) then
        minzd = zd
        minz = z
      end if
    end do
! find largest radius (we give only half of the largest side to be considered as radius)
    radius = -1.e20
    if (maxx - minx .gt. radius) then
      radiusd = maxxd - minxd
      radius = maxx - minx
    else
      radiusd = 0.0_8
    end if
    if (maxy - miny .gt. radius) then
      radiusd = maxyd - minyd
      radius = maxy - miny
    end if
    if (maxz - minz .gt. radius) then
      radiusd = maxzd - minzd
      radius = maxz - minz
    end if
    radiusd = radiusd/2.
    radius = radius/2.
